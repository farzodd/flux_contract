class Contract:
    def __init__(self, ContractName, PartyA, PartyB, ContractText, Ownership, PaymentTerms, ContractValue, DeliveryDate, ExpiryDate):
        self.ContractName = ContractName
        self.PartyA = PartyA
        self.PartyB = PartyB
        self.ContractText = ContractText
        self.Ownership = Ownership
        self.PaymentTerms = PaymentTerms
        self.ContractValue = ContractValue
        self.DeliveryDate = DeliveryDate
        self.ExpiryDate = ExpiryDate
        
